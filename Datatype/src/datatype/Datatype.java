package datatype;
public class Datatype {

    public static void main(String[] args) {
        boolean b=true;
        char c='a';
        float f=10.2f;
        double d=10.2;
        System.out.println(b);
        System.out.println("b= "+b);
        System.out.println("d= "+d);
        System.out.println("c= "+c);
        System.out.println("f= "+f);
    }
    
}
