package circle.with.math.function;
public class CircleWithMathFunction {
    public static void main(String[] args) {
       int radius=6;
        double p,a;
        p=2*Math.PI*radius;
        a=Math.PI*radius*radius;
        System.out.printf("Perimeter of the Circle = %.6f inches \n",p);
        System.out.printf("Area of the Circle = %.6f square inches \n",a);
    }
    
}
